void LCDdisplay::disLargeNum(uint8_t xPos,uint8_t yPos,int8_t data,bool color,bool fill,bool underline)
{
	int8_t temData=data;
	if(data>99||data<-99)
		return;
	if(temData==0)
	{
		writeLargeChar(xPos,yPos,' ',color,fill,underline);
		writeLargeChar(xPos,yPos+8+2*fill,'0',color,fill,underline);
		writeLargeChar(xPos,yPos+16+4*fill,'0',color,fill,underline);
	}
	else if(temData<0)
	{
		writeLargeChar(xPos,yPos,'-',color,fill,underline);
		writeLargeChar(xPos,yPos+8+2*fill,(-data)/10+0x30,color,fill,underline);
		writeLargeChar(xPos,yPos+16+4*fill,(-data)%10+0x30,color,fill,underline);
	}
	else
	{
		writeLargeChar(xPos,yPos,' ',color,fill,underline);
		writeLargeChar(xPos,yPos+8+2*fill,data/10+0x30,color,fill,underline);
		writeLargeChar(xPos,yPos+16+4*fill,data%10+0x30,color,fill,underline);
	}
}
uint8_t LCDdisplay::disMediumNum(uint8_t xPos,uint8_t yPos,int16_t data,bool color,bool fill)
{
	int16_t temData=data;
	uint8_t asc10000=' '; 
	uint8_t asc1000=' '; 
	uint8_t asc100=' '; 
	uint8_t asc10=' '; 
	uint8_t asc1=' '; 
	uint8_t len=0;
	setXY(xPos,yPos);
	if(temData==0)
	{
		writeMediumChar('0',color,fill);
		len=1;
	}
	else if(temData<0)
	{
		asc10000=(-temData)/10000+0x30;
		asc1000=(-temData%10000)/1000+0x30;
		asc100=(-temData%1000)/100+0x30;
		asc10=(-temData%100)/10+0x30;
		asc1=(-temData)%10+0x30;
		
		writeMediumChar('-',color,fill);
		len++;
		if(asc10000!='0')//万位
		{
			writeMediumChar(asc10000,color,fill);
			len++;
		}
		if(asc1000!='0'||len>1)//千位
		{
			writeMediumChar(asc1000,color,fill);
			len++;
		}
		if(asc100!='0'||len>1)//百位
		{
			writeMediumChar(asc100,color,fill);
			len++;
		}
		if(asc10!='0'||len>1)//十位
		{
			writeMediumChar(asc10,color,fill);
			len++;
		}
		writeMediumChar(asc1,color,fill);//个位
		len++;
	}
	else
	{
		asc10000=(temData)/10000+0x30;
		asc1000=(temData%10000)/1000+0x30;
		asc100=(temData%1000)/100+0x30;
		asc10=(temData%100)/10+0x30;
		asc1=(temData)%10+0x30;
		if(asc10000!='0')//万位
		{
			writeMediumChar(asc10000,color,fill);
			len++;
		}
		if(asc1000!='0'||len>0)//千位
		{
			writeMediumChar(asc1000,color,fill);
			len++;
		}
		if(asc100!='0'||len>0)//百位
		{
			writeMediumChar(asc100,color,fill);
			len++;
		}
		if(asc10!='0'||len>0)//十位
		{
			writeMediumChar(asc10,color,fill);
			len++;
		}
		writeMediumChar(asc1,color,fill);
		len++;
	}
	return len;
}





























