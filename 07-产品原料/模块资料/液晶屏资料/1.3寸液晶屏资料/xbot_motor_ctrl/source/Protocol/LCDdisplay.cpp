void LCDdisplay::sendCommand(uint8_t cmd)				//发送命令
{
	LCD_CMD();
	lcddisPort->putData(cmd);
}

void LCDdisplay::sendData(uint8_t data)						//发送数据
{
	LCD_DAT();
	lcddisPort->putData(data);
}

void LCDdisplay::enabledDisplay()						//使能显示
{
	sendCommand(0XAF);
}

void LCDdisplay::disabledDisplay()						//禁能显示
{
	sendCommand(0XAE);
}

bool LCDdisplay::setStartLine(int line)						//设置起始行地址
{
	if(line<0||line>64)
		return false;
	sendCommand((line & 0x3F) | 0x40);
	return true;
}

bool LCDdisplay::setPageAddress(uint8_t page)			//设置页地址 0XB0-0XB8
{
	if(page>8)
		return false;
	sendCommand(0xB0 | (page & 0x07));
	return true;
}

bool LCDdisplay::setColumnAddress(uint8_t column)		//设置列地址
{
	if(column>128)
		return false;
	sendCommand(0x10 | ((column >> 4) & 0x0F));  //先发高地址 高四位
	sendCommand(column & 0x0F);                  //后发低地址 低四位
	return true;
}
bool LCDdisplay::setXY(uint8_t xPos,uint8_t yPos)			//设置起始坐标
{
	if(xPos>8||yPos>128)
		return false;
	setPageAddress(xPos);  //页地址 将行分为9大行
	setColumnAddress(yPos);//列地址 
	return true;
}

void LCDdisplay::setContrast(uint8_t contrast) //设置屏幕的对比度 亮度
{
		int c = 0;
    sendCommand(0x81);
    sendCommand(c);
}

void LCDdisplay::clearScreen(uint8_t Value)//清空屏幕 Value=0X00 背景色清屏 Value=0Xff 前景色清屏
{
	int page,col;
	for(page = 0; page < 8; page++)
	{
		setPageAddress(page);
		setColumnAddress(0);
		for(col = 0; col < 132; col++)
		{
			sendData(Value);
		}
	}
}

//LCD屏初始化
void LCDdisplay::LcdInit()
{
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);
	STM32_GpioOneInit(GPIO_Pin_9,GPIO_Speed_50MHz,GPIO_Mode_Out_PP,GPIOA);
	STM32_GpioOneInit(GPIO_Pin_8,GPIO_Speed_50MHz,GPIO_Mode_Out_PP,GPIOA);
	STM32_GpioOneInit(GPIO_Pin_11,GPIO_Speed_50MHz,GPIO_Mode_Out_PP,GPIOA);
	LCD_RST_Clr();//硬复位
	SysTick_DelayMs(100);	
	LCD_RST_Set();
	
	sendCommand(0xae);//--turn off oled panel
	sendCommand(0x00);//---set low column address
	sendCommand(0x10);//---set high column address
	sendCommand(0x40);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
	sendCommand(0xa1);//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
	sendCommand(0xc8);//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
	sendCommand(0xa6);//--set normal display
	sendCommand(0xa8);//--set multiplex ratio(1 to 64)
	sendCommand(0x3f);//--1/64 duty
	sendCommand(0xd3);//-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
	sendCommand(0x00);//-not offset
	sendCommand(0xd5);//--set display clock divide ratio/oscillator frequency
	sendCommand(0x80);//--set divide ratio, Set Clock as 100 Frames/Sec
	sendCommand(0xd9);//--set pre-charge period
	sendCommand(0xf1);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	sendCommand(0xda);//--set com pins hardware configuration
	sendCommand(0x12);
	sendCommand(0xdb);//--set vcomh
	sendCommand(0x40);//Set VCOM Deselect Level
	sendCommand(0x20);//-Set Page Addressing Mode (0x00/0x01/0x02)
	sendCommand(0x02);//
	sendCommand(0x8d);//--set Charge Pump enable/disable
	sendCommand(0x14);//--set(0x10) disable
	sendCommand(0xa4);// Disable Entire Display On (0xa4/0xa5)
	sendCommand(0xa6);// Disable Inverse Display On (0xa6/a7)
	sendCommand(0xaf);//--turn on oled panel

	setStartLine(0);
	clearScreen(0X00);
	enabledDisplay();
	setContrast(0xcf);//设置对比度

}



/**
  * 功  能：显示一个中号英文字符
  * 参  数：
	* data  : 待显示的字符
	* color : true,前景色显示ffalse，背景色显示
  * 返回值：无
  */
void LCDdisplay::writeMediumChar(uint8_t data,bool color,bool fill)
{
	const uint8_t *Lib;
	uint8_t line;
	Lib=ASCII+(u16)data*6;
	uint8_t fillData=0x00;
	if(!color)
	{
		fillData=~fillData;
	}
	for (line=0; line<6; line++)
	{
		uint8_t temData=Lib[line];
		if(!color)
		{
			temData=~temData;
		}
		sendData(temData);
	}
	if(fill)
		sendData(fillData);
}

void LCDdisplay::writeMediumCharLine(uint8_t x,uint8_t y,const char* data,uint8_t len,bool color,bool fill,uint8_t start)			//显示中号字符串
{
	setXY(x,y);
	const char* tenData=data+start;
	for(int i=0;i<len;i++)
		writeMediumChar(tenData[i],color,fill);
}

void LCDdisplay::writeMediumChar_1(uint8_t xPos,uint8_t yPos,uint8_t data,bool color,bool fill,bool underline)		//居中显示中号字符6*16
{
	const uint8_t *Lib;
	uint8_t line;
	Lib=ASCII+(u16)data*6;
	uint8_t fillData_up=0x00;
	uint8_t fillData_down=0x00;

	if(underline)
	{
		fillData_down|=0x10;
	}
	if(!color)
	{
		fillData_up=0XF8;
		fillData_down=0X1F;
	}
	if(!setXY(xPos,yPos))//设置起始位置 先画上半部
		return;
	for (line=0; line<6; line++)//先画上半部分
	{
		uint8_t temData=(Lib[line]<<4)&0XF0;
		if(!color)
		{
			temData=~temData;
			temData&=0xF8;
		}
		sendData(temData);
	}
	if(fill)
		sendData(fillData_up);
	if(!setXY(xPos+1,yPos))//设置起始位置 先画上半部
		return;
	for (line=0; line<6; line++)//后画下半部分
	{
		uint8_t temData=(Lib[line]>>4)&0x0F;
		if(underline)
			temData|=0x10;
		if(!color)
		{
			temData=~temData;
			temData&=0x1F;
		}
		sendData(temData);
	}
	if(fill)
		sendData(fillData_down);
}

void LCDdisplay::writeMediumCharLine_1(uint8_t xPos,uint8_t yPos,const char* data,uint8_t len,bool color,bool fill,bool underline)			//居中显示中号字符串
{
	const char* tenData=data;
	uint8_t temxXPos=xPos;
	uint8_t temYPos=yPos;
	for(int i=0;i<len;i++)
	{
		writeMediumChar_1(temxXPos,temYPos,tenData[i],color,fill,underline);
		temYPos+=6+fill;
	}
}

